package ssh.domain;

public class EmosWorkFlow {
	private String id;
	private String name;
	private String flowid;
	private String sheetid;
	private String remark;
	private String mainservicebeanid;
	private String roleid;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFlowid() {
		return flowid;
	}
	public void setFlowid(String flowid) {
		this.flowid = flowid;
	}
	public String getSheetid() {
		return sheetid;
	}
	public void setSheetid(String sheetid) {
		this.sheetid = sheetid;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getMainservicebeanid() {
		return mainservicebeanid;
	}
	public void setMainservicebeanid(String mainservicebeanid) {
		this.mainservicebeanid = mainservicebeanid;
	}
	public String getRoleid() {
		return roleid;
	}
	public void setRoleid(String roleid) {
		this.roleid = roleid;
	}
	
	


}
